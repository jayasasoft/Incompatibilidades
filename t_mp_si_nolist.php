	// Return basic search SQL
	function BasicSearchSQL($arKeywords, $type) {
		$sWhere = "";
		$this->BuildBasicSearchSQL($sWhere, $this->Grado, $arKeywords, $type);
		$this->BuildBasicSearchSQL($sWhere, $this->Grado_Si, $arKeywords, $type);
